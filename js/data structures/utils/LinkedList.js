class DoubleLinkedListNode {
    constructor(val) {
        this.val = val;
        this.prev = null;
        this.next = null;
    }
}

 class SingleLinkedListNode {
    constructor(val) {
        this.val = val;
        this.next = null;
    }
}

export {DoubleLinkedListNode, SingleLinkedListNode}; 